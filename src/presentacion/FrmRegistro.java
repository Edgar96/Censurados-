    private void TablaPrincipalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPrincipalMouseClicked
        try {
            int row = TablaPrincipal.getSelectedRow();
            String Vnumero = (TablaPrincipal.getModel().getValueAt(row, 0).toString());
            String Vmedicamento = (TablaPrincipal.getModel().getValueAt(row, 1).toString());
            String Vgramaje = (TablaPrincipal.getModel().getValueAt(row, 2).toString());
            String Vcantidad = (TablaPrincipal.getModel().getValueAt(row, 3).toString());
            String Vobservaciones = (TablaPrincipal.getModel().getValueAt(row, 4).toString());

            txtNumero.setText(Vnumero);
            txtMedicamento.setText(Vmedicamento);
            txtGramaje.setText(Vgramaje);
            txtCantidad.setText(Vcantidad);
            txtObservaciones.setText(Vobservaciones);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Debes Completar Todos Los Campos");
        }
    }//GEN-LAST:event_TablaPrincipalMouseClicked

    private void btneliminarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarMActionPerformed

        if (!txtNumero.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el medicamento " + txtMedicamento.getText() + " ? ", "Confrimar", 2);

            if (confirmacion == 0) {
                Consultas func = new Consultas();

                boolean h = func.EliminarMedicamento(txtNumero.getText());
                if (h == true) {
                    JOptionPane.showMessageDialog(rootPane, "Se ha Eliminado el Medicamento " + txtMedicamento.getText());
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se ha Podido eliminar el Medicamento");
                }
                Limpiar();
                InfMed();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un Medicamento para eliminar");

        }

    }//GEN-LAST:event_btneliminarMActionPerformed

