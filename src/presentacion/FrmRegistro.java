    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        if (txtNumero.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un numero para el medicamento");
            txtNumero.requestFocus();
        } else if (txtMedicamento.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un nombre para el nombre ");
            txtMedicamento.requestFocus();
        } else if (txtGramaje.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un gramaje para el medicamento");
            txtGramaje.requestFocus();
        } else if (txtCantidad.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una cantidad para el medicamento");
            txtCantidad.requestFocus();
        } else if (txtObservaciones.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una observacion para el empleado");
            txtObservaciones.requestFocus();
        } else {

            Consultas RegM = new Consultas();
            String numero = txtNumero.getText();
            String medicamento = txtMedicamento.getText();
            String gramaje = txtGramaje.getText();
            String cantidad = txtCantidad.getText();
            String observaciones = txtObservaciones.getText();
            RegM.RegistrarMedicamento(numero, medicamento, gramaje, cantidad, observaciones);

            InfMed();
            Limpiar();
        }
    }//GEN-LAST:event_btnguardarActionPerformed

