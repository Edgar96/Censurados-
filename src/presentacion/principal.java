    private void menu_reporteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menu_reporteMouseClicked
       
        Map p = new HashMap();
        JasperReport report;
        JasperPrint print;
        try {
            report = JasperCompileManager.compileReport(new File("").getAbsolutePath()
                    + "/src/Reporte/Rcensurados.jrxml");
            print = JasperFillManager.fillReport(report, p, connection);
            JasperViewer view = new JasperViewer(print, false);
            view.setTitle("Reporte General");
            view.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);

        }
    }//GEN-LAST:event_menu_reporteMouseClicked

